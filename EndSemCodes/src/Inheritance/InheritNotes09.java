package Inheritance;

public class InheritNotes09 {
//	INHERITANCE IN JAVA
//
//	Meaning:
//	One class gets properties and methods of another class.
//
//	Parent class = gives
//	Child class = takes
//
//	Keyword:
//	extends
//
//	Syntax:
//
//	class Parent {
//
//	}
//
//	class Child extends Parent {
//
//	}
//
//	Advantages:
//	1. Reuse code
//	2. Less code
//	3. Easy to manage
//
//	Types:
//	1. Single inheritance
//	2. Multilevel inheritance
//	3. Hierarchical inheritance
}
