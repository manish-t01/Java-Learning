package methodsInJava;

public class NotesOnMethods07 {
	
//	        =============================
//			   METHODS IN JAVA - NOTES
//			=============================
//
//			1. What is Method?
//			------------------
//			A method is a block of code
//			that performs a specific task.
//
//			Example:
//			greet() -> says hello
//			add()   -> adds numbers
//
//			------------------
//			2. Syntax
//			------------------
//
//			public static void methodName() {
//
//			}
//
//			Example:
//
//			public static void greet() {
//			    System.out.println("Hello");
//			}
//
//			------------------
//			3. Parts of Method
//			------------------
//
//			public   -> anyone can use
//			static   -> call directly
//			void     -> no return value
//			int      -> returns integer
//			name     -> method name
//			()       -> parameters/input
//
//			------------------
//			4. Calling Method
//			------------------
//
//			greet();
//
//			------------------
//			5. Types of Methods
//			------------------
//
//			1) No parameter, no return
//
//			static void greet() {
//			    System.out.println("Hello");
//			}
//
//			2) With parameter
//
//			static void greet(String name) {
//			    System.out.println(name);
//			}
//
//			Call:
//			greet("Manish");
//
//			3) With return value
//
//			static int add(int a, int b) {
//			    return a + b;
//			}
//
//			------------------
//			6. Why use methods?
//			------------------
//
//			- Reuse code
//			- Less writing
//			- Easy to understand
//			- Better structure
//
//			------------------
//			7. Main Method
//			------------------
//
//			public static void main(String[] args)
//
//			Program starts from here.
//
//			------------------
//			8. Easy Remember
//			------------------
//
//			Method = Task
//
//			greet()    -> greeting
//			add()      -> addition
//			checkAge() -> checking age

//	        ========================
//			METHOD SYNTAX IN JAVA
//			========================
//
//			accessModifier static returnType methodName(parameters) {
//
//			    // code
//
//			}
//
//			Example:
//
//			public static void greet() {
//
//			    System.out.println("Hello");
//
//			}
//
//
//			Another example:
//
//			public static int add(int a, int b) {
//
//			    return a + b;
//
//			}
}
